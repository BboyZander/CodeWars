def what_time_is_it(angle):
    time_dic = {
        '0': '12:00',
        '30': '01:00',
        '60': '02:00',
        '90': '03:00',
        '120': '04:00',
        '150': '05:00',
        '180': '06:00',
        '210': '07:00',
        '240': '08:00',
        '270': '09:00',
        '300': '10:00',
        '330': '11:00',
        '360': '12:00'
    }
    return time_dic[str(angle)]

print(what_time_is_it(30))